package com.syntax.class12;

public class ForWithString {

	public static void main(String[] args) {
		
		String str="Good Morning Students";
		char letter;
		
		for (int i=0; i<str.length(); i++) {
			letter=str.charAt(i);
			System.out.print(letter+"");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
