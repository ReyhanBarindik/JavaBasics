package com.syntax.class09;

public class MorePatterns {

	public static void main(String[] args) {
		
		for (int i=1; i<=5; i++) {
			
			for (int j=5; j>=1; j--) {
				System.out.print(" "+j);
			}
			
			System.out.println();
		}
		
		System.out.println("_____________");
		
           for (int i=5; i>=1; i--) {
			
			for (int j=5; j>=1; j--) {
				System.out.print(" "+i);
			}
			
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
