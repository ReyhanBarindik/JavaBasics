package com.syntax.class09;

public class RowColumn {

	public static void main(String[] args) {
	   for (int row=1; row<9; row++) {
		   for (int column=1; column<=5; column++) {
			   System.out.print(column);
			   
		  }
		   System.out.println();
		   
	   }
       System.out.println("______________");
       for (int i=1; i<5; i++) {
		   for (int j=1; j<=5; j++) {
			   System.out.print(i);
			   
		  }
		   System.out.println();
		   
	   }

	   
	   
	   
	   
	   
	}

}
