package com.syntax.class03;

public class ShorthandOperators {

	public static void main(String[] args) {
		//compound shorthand assignment operators 
		int num=200;
		
		num-=100; //num=num-100
		System.out.println(num);
		
		
		int a=10;
		a*=10;
		System.out.println(a);
		
		a%=10;
		System.out.println(a);
		
		
		
		

	}

}
