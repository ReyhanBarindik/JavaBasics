package com.syntax.class03;

public class ShorthandOperators1 {

	public static void main(String[] args) {
		// 
		
		int i=5;
		System.out.println(i);
		
		i=i+1;
		System.out.println(i);
		
		i++;
		System.out.println(i);
		
		i--;
		System.out.println(i);
		
		i+=i;
		System.out.println(i);
	
		i*=i;
		System.out.println(i);
		
		
		
		
		
		

	}

}
