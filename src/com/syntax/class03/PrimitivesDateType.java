package com.syntax.class03;

public class PrimitivesDateType {

	public static void main(String[] args) {
	
		long l=10000000;
		int i=(int) l;
		//This is Explicit Casting.(larger to samller)
		System.out.println(i);
		
		
		float f=3.14f;
		int i1=(int)f;
		//Truncation
		System.out.println(i1);
		
		
		long l1=1000000;
		byte b=(byte)l1;
		
		
		System.out.println(b);
		
		
		
		
		
		
 
	}

}
